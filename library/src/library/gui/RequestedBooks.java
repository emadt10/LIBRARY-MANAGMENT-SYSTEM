    public void setTableData() {
        for (int i = 0; i < rList.size(); i++) {
            user u = new user();
            Book b = new Book();
            r = rList.get(i);
            u = r.getU();
            b = r.getB();
            DefaultTableModel model = (DefaultTableModel) tbl_requests.getModel();
            model.addRow(new Object[]{r.getID(), u.getName(), u.getAddress(), u.getPhoneno(), u.getEmail(), b.getTitle(), b.getIsbn(), r.getIssueDate(), r.getDueDate()});
        }
    }
