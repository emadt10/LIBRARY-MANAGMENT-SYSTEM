    private void btnreturnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnreturnActionPerformed

        Date dateFormat;
        dateFormat = returnb2.getDate();
        String returnDate = dateFormat.toString();
        if (returnDate != "") {
            boolean b = bfn.returnBook(titlecb.getSelectedItem().toString(), txtno.getText(), txtissueddate.getText(), returnDate);
            if (b == true) {
                JOptionPane.showMessageDialog(null, "Book Returned Notification Sent!");
            } else {
                JOptionPane.showMessageDialog(null, "Error");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please fill Return Date field!");
        }
    }//GEN-LAST:event_btnreturnActionPerformed

