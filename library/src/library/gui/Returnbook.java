    public void setComboboxData() {
        if (uibList.size() != 0) {
            for (int i = 0; i < uibList.size(); i++) {
                urb = uibList.get(i);
                b = urb.getB();
                titlecb.addItem(b.getTitle());
            }
        }
    }

