public void setTableData(){
    for (int i = 0; i < urbList.size(); i++) {
        
            Book b = new Book();
            urb = urbList.get(i);
            b=urb.getB();
            DefaultTableModel model = (DefaultTableModel) tbl_return.getModel();
            model.addRow(new Object[]{urb.getId(),b.getTitle(), b.getIsbn(), urb.getIssueDate(), urb.getReturnDate()});
        }
    }

