    private void btnsigninActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsigninActionPerformed
                boolean b= false;
        String UN=txtuname.getText();
        String UP=txtupass.getText();

        uLogin log=new uLogin();
        b=log.Loginuser(UN,UP);
        user uinfo=log.getUser();

        if(b)
        {
            if (userradiobtn.isSelected()==true && log.getRole().equals("USER"))
            {
                JOptionPane.showMessageDialog(null,"LOGGED IN");

                userd u=new userd(uinfo);
                this.setVisible(false);
                u.setVisible(true);
                b=true;
            }
            else if (libradiobtn.isSelected()==true && log.getRole().equals("LIBRARIAN"))
            {
                JOptionPane.showMessageDialog(null,"LOGGED IN");

                libf l = new libf();
                this.setVisible(false);
                l.setVisible(true);
                b=true;
            }
            else
            {
                b=false;
                JOptionPane.showMessageDialog(null,"Invalid");
            }
        }
                 

    }//GEN-LAST:event_btnsigninActionPerformed

